function translateKoCore(core: string): string {
  const exact = exactKoTranslations[core];
  if (exact) {
    return exact;
  }

  for (const [pattern, replace] of koPatternTranslations) {
    const match = core.match(pattern);
    if (match) {
      return replace(match);
    }
  }

  return core;
}

function translateCore(core: string): string {
  if (!hasKorean.test(core)) {
    return core;
  }

  const exact = exactTranslations[core];
  if (exact) {
    return exact;
  }

  for (const [pattern, replace] of patternTranslations) {
    const match = core.match(pattern);
    if (match) {
      return replace(match);
    }
  }

  let translated = core;
  for (const [source, target] of replacements) {
    translated = translated.split(source).join(target);
  }

  return translated;
}

export function translateText(value: string, language: LanguageType): string {
  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const core = value.trim();

  if (language === "ko") {
    return `${leading}${translateKoCore(core)}${trailing}`;
  }

  if (!hasKorean.test(value)) {
    return value;
  }

  return `${leading}${translateCore(core)}${trailing}`;
}
